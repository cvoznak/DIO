package dioVariaveisDadosOperadores;

/*Classe utilizada para resolução do exercício da Aula 2 de variáveis, tipos de dados e operadores aritméticos*/

public class tipos_de_dados {

	public static void main(String[] args) {
		
		byte b1 = 100;
		byte b2 = -100;
		
		short s1 = 5000;
		short s2 = -15000;
		
		int i1 = 2000000;
		int i2 = -60000000;
		
		long l1 = 86510000L;
		long l2 = 97464689746L;
		
		float f1 = 12.655f;
		float f2 = -761.6564F;
		
		double d1 = 8.5646213854;
		double d2 = -8654.57465131347d;
		
		char c1 = 'W';
		char c2 = '\u0057'; /* unicode de um caracter W*/
		
		String st1 = "Casa";
		String st2 = "O lago azul";
		
		boolean bo1 = true;
		boolean bo2 = false;
		
		System.out.println(b1);
		System.out.println(b2);
		
		System.out.println(s1);
		System.out.println(s2);
		
		System.out.println(i1);
		System.out.println(i2);
		
		System.out.println(l1);
		System.out.println(l2);
		
		System.out.println(f1);
		System.out.println(f2);
		
		System.out.println(d1);
		System.out.println(d2);
		
		System.out.println(c1);
		System.out.println(c2);
		
		System.out.println(st1);
		System.out.println(st2);
		
		System.out.println(bo1);
		System.out.println(bo2);
	}

}
